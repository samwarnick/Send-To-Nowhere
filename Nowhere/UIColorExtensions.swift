import UIKit

extension UIColor {
    class var stnCyan: UIColor {
        return UIColor(red: 8.0 / 255.0, green: 178.0 / 255.0, blue: 227.0 / 255.0, alpha: 1.0)
    }
    
    class var stnRichElectricBlue: UIColor {
        return UIColor(red: 0.0 / 255.0, green: 155.0 / 255.0, blue: 211.0 / 255.0, alpha: 1.0)
    }
    
    class var stnColumbiaBlue: UIColor {
        return UIColor(red: 197.0 / 255.0, green: 213.0 / 255.0, blue: 234.0 / 255.0, alpha: 1.0)
    }
}
